package animales;

public class Perro extends Canido {
	
	public String getSonido() {   /*Método que devuelve un String con el sonido de un perro*/
		return "Ladrido";
	}
	
	public String getAlimentos() { /*Método que devuelve un String con los alimentos de un perro*/
		return "Carnívoro";
	}
	
	public String getHabitat() { /*Método que devuelve un String con el hábitat de un perro*/
		return "Doméstico";
	}
	
	public String getNombreCientifico() {  /*Método que devuelve un String con el nombre científico de un perro*/
		return "Canis lupus familiaris";
	}
	
}
