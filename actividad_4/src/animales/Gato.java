package animales;

public class Gato extends Felino{
	
	public String getSonido() {  /*Método que devuelve un String con el sonido de un gato*/
		return "Maullido";
	}
	
	public String getAlimentos() {  /*Método que devuelve un String con los alimentos de un gato*/
		return "Ratones";
	}
	
	public String getHabitat() {  /*Método que devuelve un String con el hábitat de un gato*/
		return "Doméstico";
	}
	
	public String getNombreCientifico() {  /*Método que devuelve un String con el nombre científico de un gato*/
		return "Felis silvestris catus";
	}
	

}
