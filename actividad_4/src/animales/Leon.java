package animales;

public class Leon extends Felino{
	
	public String getSonido() {  /*Método que devuelve un String con el sonido de un león*/
		return "Rugido";
	}
	
	public String getAlimentos() {  /*Método que devuelve un String con los alimentos de un león*/
		return "Carnívoro";
	}
	
	public String getHabitat() { /*Método que devuelve un String con el hábitat de un león*/
		return "Praderas";
	}
	
	public String getNombreCientifico() {  /*Método que devuelve un String con el nombre científico de un león*/
		return "Panthera leo";
	}

}
