package animales;

public class Lobo extends Canido {
	
	public String getSonido() { /*Método que devuelve un String con el sonido de un lobo*/
		return "Aullido";
	}
	
	public String getAlimentos() { /*Método que devuelve un String con los alimentos de un lobo*/
		return "Carnívoro";
	}
	
	public String getHabitat() { /*Método que devuelve un String con el hábitat de un lobo*/
		return "Bosque";
	}
	
	public String getNombreCientifico() { /*Método que devuelve un String con el nombre científico de un lobo*/
		return "Canis lupus";
	}

}
